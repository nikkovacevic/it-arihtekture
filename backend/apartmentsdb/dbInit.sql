\c postgres;

DROP DATABASE apartments;

CREATE DATABASE apartments;

\c apartments;

CREATE TABLE apartments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    link VARCHAR(255),
    title VARCHAR(255),
    location VARCHAR(255),
    price VARCHAR(255),
    image VARCHAR(255),
    CONSTRAINT pk_apartments PRIMARY KEY (id)
);

